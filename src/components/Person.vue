<script lang="ts">
export default {
  name: 'Person',
  data() {
    return {
      name: 'zzz',
      age: 18,
      tel: '110'
    }
  },
  methods: {
    showTel() {
      alert(this.tel)
    },
    changeName() {
      this.name = 'aaa'
    },
    changeAge() {
      this.age++
    }
  }
}
</script>

<template>
  <div class="person">
    <h2>姓名：{{ name }}</h2>
    <h2>年龄：{{ age }}</h2>
    <button @click="changeName">修改名字</button>
    <button @click="changeAge">修改年龄</button>
    <button @click="showTel">查看联系方式</button>
  </div>
</template>

